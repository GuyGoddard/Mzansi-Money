// ── Engine ──
let currentLang = (() => {
  try { return localStorage.getItem('mmg_lang') || 'en'; } catch(e) { return 'en'; }
})();

function setLang(lang) {
  currentLang = lang;
  try { localStorage.setItem('mmg_lang', lang); } catch(e) {}
  applyTranslations();
  updateLangButtons();
  // Update html lang attribute
  document.documentElement.lang = lang === 'zu' ? 'zu' : lang === 'xh' ? 'xh' : lang === 'af' ? 'af' : 'en';
}

function t(key) {
  return (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key] !== undefined)
    ? TRANSLATIONS[currentLang][key]
    : (TRANSLATIONS['en'][key] !== undefined ? TRANSLATIONS['en'][key] : key);
}

function applyTranslations() {
  document.querySelectorAll('[data-t]').forEach(el => {
    const key = el.getAttribute('data-t');
    const attr = el.getAttribute('data-t-attr');
    if (attr) {
      el.setAttribute(attr, t(key));
    } else {
      el.textContent = t(key);
    }
  });
  // Also update page title if it has a data-t-title attribute on <html>
  const titleKey = document.documentElement.getAttribute('data-t-title');
  if (titleKey) document.title = t(titleKey);
}

function updateLangButtons() {
  document.querySelectorAll('[data-lang]').forEach(btn => {
    const lang = btn.getAttribute('data-lang');
    const isActive = lang === currentLang;
    btn.className = 'lang-btn ' + (isActive ? 'bg-primary' : 'bg-surface-container-high');
  });
}

document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
  updateLangButtons();
});
